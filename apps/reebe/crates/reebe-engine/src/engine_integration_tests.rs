    #[tokio::test]
    async fn test_straight_through_process_completes() {
        let bpmn = wrap_process("proc", r#"
    <bpmn:startEvent id="start"><bpmn:outgoing>f1</bpmn:outgoing></bpmn:startEvent>
    <bpmn:endEvent id="end"><bpmn:incoming>f1</bpmn:incoming></bpmn:endEvent>
    <bpmn:sequenceFlow id="f1" sourceRef="start" targetRef="end"/>
"#);
        let h = Harness::new();
        h.deploy(&bpmn).await;
        h.start("proc", serde_json::json!({})).await;

        assert_eq!(h.process_state("proc").as_deref(), Some("COMPLETED"),
            "process should reach COMPLETED; instances: {:?}", h.process_instances());
        assert!(h.visited("start"), "start must be visited");
        assert!(h.visited("end"), "end must be visited");
    }

