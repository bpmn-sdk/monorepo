    // ─────────────────────────────────────────────────────────────────
    // Test harness
    // ─────────────────────────────────────────────────────────────────

    struct Harness {
        pub backend: Arc<InMemoryBackend>,
        state: Arc<EngineState>,
        processors: Vec<Arc<dyn RecordProcessor>>,
    }

    impl Harness {
        fn new() -> Self {
            let backend = Arc::new(InMemoryBackend::new());
            let clock = Arc::new(VirtualClock::new(chrono::Utc::now()));
            let state = Arc::new(EngineState {
                backend: backend.clone() as Arc<dyn reebe_db::StateBackend>,
                partition_id: 0,
                process_def_cache: crate::process_def_cache::ProcessDefCache::new(),
                clock,
            });
            let job_notifier = Arc::new(JobNotifier::new());
            let processors: Vec<Arc<dyn RecordProcessor>> = vec![
                Arc::new(DeploymentProcessor),
                Arc::new(ProcessInstanceCreationProcessor),
                Arc::new(BpmnElementProcessor),
                Arc::new(JobProcessor { job_notifier }),
                Arc::new(VariableDocumentProcessor),
                Arc::new(UserTaskProcessor),
                Arc::new(SignalProcessor),
                Arc::new(TimerProcessor),
                Arc::new(MessageProcessor),
            ];
            Self { backend, state, processors }
        }

        /// Submit a command and drain all follow-up commands until quiescent.
        async fn run(
            &self,
            value_type: &str,
            intent: &str,
            payload: Value,
        ) -> Option<Value> {
            let (position, key) = self.backend.next_position_and_key(0).await.unwrap();
            let record = DbRecord {
                partition_id: 0,
                position,
                record_type: "COMMAND".to_string(),
                value_type: value_type.to_string(),
                intent: intent.to_string(),
                record_key: key,
                timestamp_ms: chrono::Utc::now().timestamp_millis(),
                payload,
                source_position: None,
                tenant_id: "<default>".to_string(),
            };
            self.backend.insert_record(&record).await.unwrap();

            let mut last_pos = position - 1;
            let mut response: Option<Value> = None;

            loop {
                let cmds = self.backend.fetch_commands_from(0, last_pos + 1, 200).await.unwrap();
                if cmds.is_empty() { break; }
                for rec in &cmds {
                    last_pos = rec.position;
                    for proc in &self.processors {
                        if proc.accepts(&rec.value_type, &rec.intent) {
                            let mut writers = Writers::new();
                            proc.process(rec, &self.state, &mut writers).await.unwrap();
                            if rec.position == position {
                                response = writers.response.clone();
                            }
                            let total = writers.events.len() + writers.commands.len();
                            if total > 0 {
                                let first = self.backend.next_position_batch(0, total).await.unwrap();
                                let mut recs = Vec::with_capacity(total);
                                for (i, ev) in writers.events.iter().enumerate() {
                                    recs.push(DbRecord {
                                        partition_id: 0,
                                        position: first + i as i64,
                                        record_type: "EVENT".to_string(),
                                        value_type: ev.value_type.clone(),
                                        intent: ev.intent.clone(),
                                        record_key: ev.key,
                                        timestamp_ms: chrono::Utc::now().timestamp_millis(),
                                        payload: ev.payload.clone(),
                                        source_position: Some(rec.position),
                                        tenant_id: rec.tenant_id.clone(),
                                    });
                                }
                                for (i, cmd) in writers.commands.iter().enumerate() {
                                    recs.push(DbRecord {
                                        partition_id: 0,
                                        position: first + writers.events.len() as i64 + i as i64,
                                        record_type: "COMMAND".to_string(),
                                        value_type: cmd.value_type.clone(),
                                        intent: cmd.intent.clone(),
                                        record_key: cmd.key,
                                        timestamp_ms: chrono::Utc::now().timestamp_millis(),
                                        payload: cmd.payload.clone(),
                                        source_position: Some(rec.position),
                                        tenant_id: rec.tenant_id.clone(),
                                    });
                                }
                                self.backend.insert_records_batch(&recs).await.unwrap();
                            }
                            break;
                        }
                    }
                }
            }
            response
        }

        /// Deploy an XML resource (BPMN or DMN — detected automatically).
        async fn deploy(&self, xml: &str) -> Value {
            let encoded = base64::engine::general_purpose::STANDARD.encode(xml.as_bytes());
            let name = if xml.contains("<decision") { "resource.dmn" } else { "resource.bpmn" };
            self.run("DEPLOYMENT", "CREATE", serde_json::json!({
                "resources": [{ "name": name, "content": encoded }]
            })).await.expect("deploy must return a response")
        }

        /// Start a process instance.
        async fn start(&self, process_id: &str, variables: Value) {
            self.run("PROCESS_INSTANCE_CREATION", "CREATE", serde_json::json!({
                "bpmnProcessId": process_id,
                "version": -1,
                "variables": variables,
            })).await;
        }

        /// Complete the first ACTIVATABLE job whose `job_type` matches.
        async fn complete_job(&self, job_type: &str, outputs: Value) {
            let job = self.activatable_job(job_type)
                .unwrap_or_else(|| panic!("no activatable job of type '{job_type}'"));
            self.run("JOB", "COMPLETE", serde_json::json!({
                "jobKey": job.key.to_string(),
                "variables": outputs,
            })).await;
        }

        /// Fail the first ACTIVATABLE job whose `job_type` matches (0 retries → incident).
        async fn fail_job(&self, job_type: &str, retries: i32, msg: &str) {
            let job = self.activatable_job(job_type)
                .unwrap_or_else(|| panic!("no activatable job of type '{job_type}'"));
            self.run("JOB", "FAIL", serde_json::json!({
                "jobKey": job.key.to_string(),
                "retries": retries,
                "errorMessage": msg,
            })).await;
        }

        fn activatable_job(&self, job_type: &str) -> Option<Job> {
            self.backend.list_jobs().into_iter()
                .find(|j| j.job_type == job_type && j.state == "ACTIVATABLE")
        }

        fn get_var(&self, name: &str) -> Option<Value> {
            self.backend.list_variables().into_iter()
                .find(|v| v.name == name)
                .map(|v| v.value)
        }

        fn process_instances(&self) -> Vec<ProcessInstance> {
            self.backend.list_process_instances()
        }

        fn process_state(&self, process_id: &str) -> Option<String> {
            self.backend.list_process_instances().into_iter()
                .find(|p| p.bpmn_process_id == process_id)
                .map(|p| p.state)
        }

        fn incidents(&self) -> Vec<Incident> {
            self.backend.list_incidents()
        }

        /// Returns the set of element IDs that have been COMPLETED.
        fn completed_elements(&self) -> Vec<String> {
            self.backend.list_element_instances().into_iter()
                .filter(|e| e.state == "COMPLETED")
                .map(|e| e.element_id)
                .collect()
        }

        fn visited(&self, element_id: &str) -> bool {
            self.backend.list_element_instances().iter()
                .any(|e| e.element_id == element_id)
        }

        /// Returns all user tasks in CREATED state for the given element ID.
        fn pending_user_tasks(&self, element_id: &str) -> Vec<UserTask> {
            self.backend.list_user_tasks().into_iter()
                .filter(|t| t.element_id == element_id && t.state == "CREATED")
                .collect()
        }

        /// Complete the first CREATED user task for `element_id` with `variables`.
        async fn complete_user_task(&self, element_id: &str, variables: Value) {
            let task = self.pending_user_tasks(element_id)
                .into_iter()
                .next()
                .unwrap_or_else(|| panic!("no pending user task at element '{element_id}'"));
            self.run("USER_TASK", "COMPLETE", serde_json::json!({
                "userTaskKey": task.key.to_string(),
                "variables": variables,
            })).await;
        }

        /// Broadcast a signal by name to all waiting catch events.
        async fn broadcast_signal(&self, signal_name: &str) {
            self.run("SIGNAL", "BROADCAST", serde_json::json!({
                "signalName": signal_name,
                "variables": {},
            })).await;
        }

        /// Publish a message that correlates waiting IntermediateCatchEvent / ReceiveTask subscriptions.
        async fn publish_message(&self, message_name: &str, correlation_key: &str, variables: Value) {
            self.run("MESSAGE", "PUBLISH", serde_json::json!({
                "messageName": message_name,
                "correlationKey": correlation_key,
                "variables": variables,
                "timeToLive": 3600000,
            })).await;
        }

        /// Find the first ACTIVE timer in the backend and fire it.
        async fn trigger_pending_timer(&self) -> Option<Timer> {
            let timer = self.backend.list_timers().into_iter()
                .find(|t| t.state == "ACTIVE");
            if let Some(ref t) = timer {
                self.run("TIMER", "TRIGGER", serde_json::json!({
                    "timerKey": t.key.to_string(),
                })).await;
            }
            timer
        }
    }

